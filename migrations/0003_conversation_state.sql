-- ============================================================================
-- حالة المحادثة (Conversation State) — لدعم الحوار التفاعلي
-- عندما تكون بيانات العملية ناقصة، نحفظ السياق هنا وننتظر رد المستخدم.
-- ============================================================================

CREATE TABLE IF NOT EXISTS conversation_state (
  chat_id       TEXT PRIMARY KEY,        -- معرّف محادثة تليجرام
  pending_json  TEXT,                     -- سياق العملية المعلّقة (JSON) + الرسائل المتراكمة
  updated_at    TEXT NOT NULL DEFAULT (datetime('now'))
);
